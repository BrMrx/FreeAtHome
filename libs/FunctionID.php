    public static function GetName( string $a_Id ) : string
    {
        $lFunctionId = hexdec( $a_Id );

        foreach( self::mMapValues as $lName => $lVal )
		{
			if( $lVal['ID'] == $lFunctionId )
			{
				return $lName;
			}
		}

        return '['.$a_Id.']';      
    }
		
    public static function IsSupportedID( string $a_Id ) : bool
    {
        $lFunctionId = hexdec( $a_Id );
        return in_array($lFunctionId, self::SupportedIDs );
    }

	public static function FilterSupportedChannels( $a_Channels ) : array
	{
        $lResult = array();
		foreach($a_Channels as $lChannelNr => $lValue)			
		{             			
            $lChannelValue = (object)$lValue;

   			if( isset($lChannelValue->functionID )  )
            {
 
                // Nur Werte mit unterstützten Eingängen zulassen
                $lInputsArry = PID::FilterSupportedType($lChannelValue,'inputs');

                IPS_LogMessage( 0, __FUNCTION__.' '.$lChannelValue->displayName.' '.self::GetName($lChannelValue->functionID).": ".json_encode($lInputsArry) );
                 
                if( !empty($lInputsArry) ) 
                {
                    $lResult[$lChannelNr]= $lChannelValue;
                }
            }
        }	
  		return $lResult;
	}
